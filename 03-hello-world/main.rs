// This is a simple Rust program that prints "Hello, world!" to the console.
// It demonstrates the basic structure of a Rust program and how to output text.

// The main function is the entry point of every Rust program.
fn main() {
    // println! is a macro in Rust used to print text to the console, followed by a newline.
    // The exclamation mark (!) indicates that println is a macro, not a regular function.
    // The string inside the parentheses is what will be printed.
    println!("Hello, world!");
}
