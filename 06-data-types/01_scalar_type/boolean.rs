// This Rust program demonstrates the boolean type.
//
// To build the project (from 06-data-types):
//    cargo build
//
// To run a specific file (like this one) with Cargo:
//    cargo run --bin boolean
//
// Booleans are either true or false.
fn main() {
    let is_available: bool = true;
    println!("The value of is_available is: {is_available}");

    let is_done = false;
    println!("The value of is_done is: {is_done}");
}
